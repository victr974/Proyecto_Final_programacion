     private void cargarTabla(){
        
        DefaultTableModel modeloTabla = (DefaultTableModel) tblCategoria.getModel();
        modeloTabla.setRowCount(0);
        int[] anchos = {5,8,25,15};
        for (int i=0; i < tblCategoria.getColumnCount(); i++ )
        tblCategoria.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
       
            try {
            conn= conectar.getConection();
            ps = conn.prepareCall("call consultarCategoria (1)");
             
            rs = ps.executeQuery();
            rsmd = (ResultSetMetaData) rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
             while(rs.next())
             {
                 Object[] fila = new Object[columnas];
                 for(int indice=0; indice<columnas; indice++){
                     fila[indice] = rs.getObject(indice + 1);
                 }
                 modeloTabla.addRow(fila);
             }
                  
        } 
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
    
