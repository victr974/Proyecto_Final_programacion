    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    int codigo = Integer.parseInt(txtCodigo.getText());
         
         try {
            conn = conectar.getConection();
            ps = conn.prepareCall("call buscar_categoria (?)");
            ps.setInt(1, codigo);
            
            rs = ps.executeQuery();
            if(rs.next())
            {
                txtId.setText(rs.getString("id_categoria"));
                txtCodigo.setText(rs.getString("codigo"));
                txtNombre.setText(rs.getString("nombre"));
                txtIdMenu.setText(rs.getString("id_menu"));
                txtEstatus.setText(rs.getString("estatus"));
                
              
                }
            else{
                JOptionPane.showMessageDialog(null, "No esiste un registro con este Id");

            }
        } catch (HeadlessException | SQLException e) {
            System.err.println(e);
        }        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

