    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
           
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtId.getText();
        int id_menu = Integer.parseInt(txtIdMenu.getText());
        
        try {
            conn = conectar.getConection();
            ps = conn.prepareCall(" call insertar_categoria(?,?,?,?)");
            ps.setInt(1, codigo);
            ps.setString(2, nombre);
            ps.setInt(3, id_menu);
            ps.setInt(4, 1);
            
            int res = ps.executeUpdate();
            if(res >0)
            {
                JOptionPane.showMessageDialog(null,"Registro guardado" );
                LimpiarCajas();
                 
            }

            else {
                JOptionPane.showMessageDialog(null, "Error al guardar empleado");
                LimpiarCajas();
               

            }
            conn.close();
        } catch (HeadlessException | SQLException e) {
            System.err.println(e);
        }        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        int codigo = Integer.parseInt(txtCodigo.getText());
         
         try {
            conn = conectar.getConection();
            ps = conn.prepareCall("call buscar_categoria (?)");
            ps.setInt(1, codigo);
            
            rs = ps.executeQuery();
            if(rs.next())
            {
                txtId.setText(rs.getString("id_categoria"));
                txtCodigo.setText(rs.getString("codigo"));
                txtNombre.setText(rs.getString("nombre"));
                txtIdMenu.setText(rs.getString("id_menu"));
                txtActi.setText(rs.getString("estatus"));
                
                }
            else{
                JOptionPane.showMessageDialog(null, "No esiste un registro con este codigo");

            }
        } catch (HeadlessException | SQLException e) {  
            System.err.println(e);
        }
  
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
       
        int id_categoria = Integer.parseInt(txtId.getText());
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        int id_menu = Integer.parseInt(txtIdMenu.getText());
        int estatus = Integer.parseInt(txtActi.getText());
        
        try {
            
            conn = conectar.getConection();
            ps = conn.prepareCall("call modificar_categoria(?,?,?,?,?)");
            ps.setInt(1, codigo);
            ps.setString(2, nombre);
            ps.setInt(3, id_menu);
            ps.setInt(4, estatus);
            ps.setInt(5, id_categoria);
            
            int res = ps.executeUpdate();
            if(res >0)
            {
                JOptionPane.showMessageDialog(null, "Registro modificado");
                LimpiarCajas();
                
            }
             else {
                JOptionPane.showMessageDialog(null, "Error al modificar registro");
                LimpiarCajas();
                 
            }
            conn.close();
        } catch (HeadlessException | SQLException e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

